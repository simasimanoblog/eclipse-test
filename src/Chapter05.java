
public class Chapter05 {

	public static void main(String[] args) {
		
		// 「String」型の変数を宣言し、自分の名前を代入
		String name = "SatoshiShimaya";
		// 自分の名前を出力
		System.out.println(name);
		// 「String」型の変数に、好きな言葉を再代入
		name = "Nullbarich";
		// 好きな言葉を出力
		System.out.println(name);
		// 「int」型の定数を宣言し、ラッキーナンバーを代入
		final int luckyNumber = 5050;
		// ラッキーナンバーを出力
		System.out.println(luckyNumber);
		
	}

}
